# Operadores básicos
# ------------------

# Soma - Resultado: 4
print(2 + 2)

# Subtração - Resultado 2
print(5 - 3)

# Multiplicação - Resultado 10
print(5 * 2)

# Divisão - Resultado 3.0
print(6 / 2)

# Divisão Inteira - Resultado 2
print(5 // 2)

# Restante (Módulo) - Resultado 1
print(7 % 3)

# Exponenciação - Resultado 9
print(3 ** 2)

print(("+" + "-"*10 + "+\n" + ("|" + " "*10 + "|\n")*5 + "+" + "-"*10 + "+\n"))