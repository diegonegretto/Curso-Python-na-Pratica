# Looping For
# -----------------

# Fazendo um looping for de 0 até 10
for i in range(11):
    print(f"O valor atual de i é: {i}")

# Fazendo um loopping com início e fim definidos
for i in range(1, 11):
    print(f"O valor atual de i é: {i}")

# Fazendo um looping com ínicio, fim e passo definidos
for i in range(1, 11, 2):
    print(f"O valor atual de i é: {i}")

# Fazendo um looping de 10 a 1 
for i in range(10, 0, -1):
    print(f"O valor atual de i é: {i}")

