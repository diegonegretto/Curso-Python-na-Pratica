# Tipagem Dinâmica
# -----------------

# Declarando uma variável chama x com o valor 10
x = 10

# A função type() mostra o tipo de dado de uma variável
# Nesse momento, x é do tipo inteiro e seu conteúdo é 10
print(type(x))
print(x)

# Atribuindo a string "Maria" para a variável x
x = "Maria"

# Agora x é do tipo string e seu conteúdo é Maria
print(type(x))
print(x)

